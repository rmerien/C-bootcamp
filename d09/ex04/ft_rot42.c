char	*ft_rot42(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			if (str[i] + 16 > 'z')
				str[i] -= 10;
			else
				str[i] += 16;
		}
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			if (str[i] + 16 > 'Z')
				str[i] -= 10;
			else
				str[i] += 16;
		}
		i++;
	}
	return (str);
}

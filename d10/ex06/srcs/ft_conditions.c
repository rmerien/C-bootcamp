#include "ft.h"

int		is_number(char c)
{
	return ((c > 47 && c < 58) ? 1 : 0);
}

int		is_op(char c)
{
	return ((c < 5 && c >= 0) ? 1 : 0);
}

int		is_neg(char c)
{
	return ((c == '-') ? 1 : 0);
}

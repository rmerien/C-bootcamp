int		ft_strncmp(char *n1, char *n2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (n1[i] != '\0' && n2[i] != '\0' && n > 0)
	{
		if (n1[i] != n2[i])
			return (n1[i] - n2[i]);
		i++;
		n--;
	}
	return (n1[i] - n2[i]);
}

char	*ft_strncat(char *dest, char *src, int nb)
{
	int n;
	int i;

	i = 0;
	n = 0;
	while (dest[i] != '\0')
		i++;
	while (src[n] != '\0' && nb > 0)
	{
		dest[i] = src[n];
		i++;
		n++;
		nb--;
	}
	return (dest);
}

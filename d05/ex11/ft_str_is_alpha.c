int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while ((str[i] < 91 && str[i] > 64) || (str[i] < 123 && str[i] > 96))
		i++;
	if (str[i] == '\0')
		return (1);
	return (0);
}

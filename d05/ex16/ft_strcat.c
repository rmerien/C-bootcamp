char	*ft_strcat(char *dest, char *src)
{
	int n;
	int i;

	i = 0;
	n = 0;
	while (dest[i] != '\0')
		i++;
	while (src[n] != '\0')
	{
		dest[i] = src[n];
		i++;
		n++;
	}
	dest[i] = src[n];
	return (dest);
}

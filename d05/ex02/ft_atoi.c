int		ft_atoi(char *str)
{
	int		atoi;
	int		neg;
	int		n;

	n = 0;
	neg = 1;
	atoi = 0;
	while ((str[n] <= 8 && str[n] >= 14) || str[n] == 32)
		n++;
	if (str[n] == '-')
		neg = -1;
	if (str[n] == '-' || str[n] == '+')
		n++;
	while (str[n] <= '9' && str[n] >= '0')
	{
		atoi = atoi * 10;
		atoi = atoi + (str[n] - 48) * neg;
		n++;
	}
	return (atoi);
}
